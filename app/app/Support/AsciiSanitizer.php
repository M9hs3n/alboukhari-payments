<?php

namespace App\Support;

/**
 * تحويل النصوص لـ ASCII لإطالة الرسالة (GSM-7) ومنعها من التحول لـ Unicode segments قصيرة.
 * مطابق لمنطق السكربت الحالي asciiSanitize_().
 */
class AsciiSanitizer
{
    private const REPLACE_MAP = [
        '’' => "'", '‘' => "'",
        '“' => '"', '”' => '"',
        '—' => '-', '–' => '-',
        '…' => '...',
        '€' => 'EUR', '£' => 'GBP',
        '•' => '-',
        'À' => 'A', 'Á' => 'A', 'Â' => 'A', 'Ã' => 'A', 'Ä' => 'A', 'Å' => 'A', 'Æ' => 'AE',
        'à' => 'a', 'á' => 'a', 'â' => 'a', 'ã' => 'a', 'ä' => 'a', 'å' => 'a', 'æ' => 'ae',
        'Ç' => 'C', 'ç' => 'c',
        'È' => 'E', 'É' => 'E', 'Ê' => 'E', 'Ë' => 'E',
        'è' => 'e', 'é' => 'e', 'ê' => 'e', 'ë' => 'e',
        'Ì' => 'I', 'Í' => 'I', 'Î' => 'I', 'Ï' => 'I',
        'ì' => 'i', 'í' => 'i', 'î' => 'i', 'ï' => 'i',
        'Ñ' => 'N', 'ñ' => 'n',
        'Ò' => 'O', 'Ó' => 'O', 'Ô' => 'O', 'Õ' => 'O', 'Ö' => 'O', 'Ø' => 'O',
        'ò' => 'o', 'ó' => 'o', 'ô' => 'o', 'õ' => 'o', 'ö' => 'o', 'ø' => 'o',
        'Ù' => 'U', 'Ú' => 'U', 'Û' => 'U', 'Ü' => 'U',
        'ù' => 'u', 'ú' => 'u', 'û' => 'u', 'ü' => 'u',
        'Ý' => 'Y', 'ý' => 'y', 'ÿ' => 'y',
        'ß' => 'ss',
    ];

    public static function sanitize(?string $text): string
    {
        if (!$text) return '';

        // استبدال الأحرف المعروفة
        $t = strtr($text, self::REPLACE_MAP);

        // إزالة أي أحرف غير ASCII
        $t = preg_replace('/[^\x00-\x7F]/', '', $t);

        // تنظيف الفراغات المتعددة
        $t = preg_replace('/\s+/', ' ', trim($t));

        return $t;
    }
}
